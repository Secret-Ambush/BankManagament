    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        if(catID.getText().equals("")||catName.getText().equals("")||catIC.getText().equals("")) {
            JOptionPane.showMessageDialog(this,"Please fill in all the fields");
        }
        else
        {
            String data[]={catID.getText(),catName.getText(),catIC.getText()};
            DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
            model.addRow(data);
            catID.setText("");
            catName.setText("");
            catIC.setText("");
        }
    }//GEN-LAST:event_addActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
        if (jTable2.getSelectedRowCount()==1)    
        {
            String ci = catID.getText();
            String cn = catName.getText();
            String ic = catIC.getText();
        
            model.setValueAt(ci,jTable2.getSelectedRow(),0);
            model.setValueAt(cn,jTable2.getSelectedRow(),1);
            model.setValueAt(ic,jTable2.getSelectedRow(),2);
            
            JOptionPane.showMessageDialog(this,"Updated successfully");
            
        }
        else
        {
            if (jTable2.getRowCount()==0)
            {
                JOptionPane.showMessageDialog(this,"Table is empty");  
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Please select single row to update");  
            }
        }
        
    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
        if(jTable2.getSelectedRowCount()==1)
        {
            model.removeRow(jTable2.getSelectedRow());
           
        }
        else
        {
            if(jTable2.getRowCount()==0)
            {
                JOptionPane.showMessageDialog(this,"Table is empty"); 
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Please select single row to delete");  
            }
        }
    }//GEN-LAST:event_deleteActionPerformed

