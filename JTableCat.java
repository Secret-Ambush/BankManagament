    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        DefaultTableModel tblmodel = (DefaultTableModel)jTable2.getModel();
        String catID_entry = tblmodel.getValueAt(jTable2.getSelectedRow(),0).toString();
        String catName_entry = tblmodel.getValueAt(jTable2.getSelectedRow(),1).toString();
        String catIC_entry = tblmodel.getValueAt(jTable2.getSelectedRow(),2).toString();
        
        catID.setText(catID_entry);
        catName.setText(catName_entry);
        catIC.setText(catIC_entry);
        
        
    }//GEN-LAST:event_jTable2MouseClicked

